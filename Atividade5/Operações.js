var numero1 = prompt("Digite o numero 1: ");
var numero2 = prompt("Digite o numero 2: ");

var soma = parseFloat(numero1) + parseFloat(numero2);
var subtração = parseFloat(numero1) - parseFloat(numero2);
var produto = parseFloat(numero1) * parseFloat(numero2);
var divisão = parseFloat(numero1) / parseFloat(numero2);  
var resto = parseFloat(numero1) % parseFloat(numero2);

alert(soma + "\n" + subtração + "\n" + produto + "\n" + divisão + "\n" + resto);


        
          